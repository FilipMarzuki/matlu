/**
 * Scans public/assets/packs/ and writes public/assets/manifest.json
 * so tools and agents can browse available art without opening every folder.
 *
 * Run: npm run assets:manifest
 * (Re-run after adding or renaming files under packs/.)
 */
import { readdirSync, statSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const projectRoot = join(__dirname, "..");
const packsDir = join(projectRoot, "public", "assets", "packs");
const outFile = join(projectRoot, "public", "assets", "manifest.json");

const ASSET_EXT = /\.(png|webp|jpg|jpeg|gif|ogg|mp3|wav|json|tmx|tsx|xml)$/i;

function walkFiles(dir) {
  /** @type {string[]} */
  const files = [];
  if (!existsSync(dir)) return files;
  for (const name of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, name.name);
    if (name.isDirectory()) files.push(...walkFiles(full));
    else if (ASSET_EXT.test(name.name)) files.push(full);
  }
  return files;
}

function main() {
  if (!existsSync(packsDir)) {
    mkdirSync(packsDir, { recursive: true });
  }

  /** @type {{ id: string; path: string; assets: { relative: string; url: string }[] }[] }} */
  const packs = [];

  if (existsSync(packsDir)) {
    for (const name of readdirSync(packsDir, { withFileTypes: true })) {
      if (!name.isDirectory()) continue;
      const packRoot = join(packsDir, name.name);
      const absFiles = walkFiles(packRoot);
      const assets = absFiles
        .map((abs) => {
          const rel = relative(packsDir, abs).replace(/\\/g, "/");
          return {
            relative: rel,
            url: `/assets/packs/${rel}`,
          };
        })
        .sort((a, b) => a.relative.localeCompare(b.relative));

      packs.push({
        id: name.name,
        path: `public/assets/packs/${name.name}`,
        assets,
      });
    }
  }

  packs.sort((a, b) => a.id.localeCompare(b.id));

  const manifest = {
    generatedAt: new Date().toISOString(),
    note:
      "Auto-generated by npm run assets:manifest — lists files under public/assets/packs/",
    baseUrl: "/assets/packs",
    packs,
  };

  writeFileSync(outFile, JSON.stringify(manifest, null, 2) + "\n", "utf8");
  const total = packs.reduce((n, p) => n + p.assets.length, 0);
  console.log(`Wrote ${outFile} (${packs.length} pack(s), ${total} asset file(s))`);
}

main();
